    private void finalizarPrioridadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_finalizarPrioridadActionPerformed
        String nomDependencia = this.dependenciaVisualizar.getText(); //obtenemos dependencia escrita en label
        Dependencia actual = this.adm.obtenerDependencia(nomDependencia); //obtenemos dependencia puesta por adm -> de la listaDependencias de la clase AdmDelSistema
        
        if (actual != null) {
            actual.finalizarTramitePrioridad();
            JOptionPane.showMessageDialog(this, "Se ha eliminado expediente", "Éxito", JOptionPane.INFORMATION_MESSAGE);
        }
        else{
            JOptionPane.showMessageDialog(this, "No existe dependencia", "Error", JOptionPane.ERROR_MESSAGE);
            //ERROR NO EXISTE DEPENDENCIA
        }
        //MENSAJE: SE HA ELIMINADO...
        VisualizarDependencia frame = new VisualizarDependencia(this.adm);
        frame.setVisible(true);
        this.dispose();
    }//GEN-LAST:event_finalizarPrioridadActionPerformed

    private void finalizarIDActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_finalizarIDActionPerformed
        String nomDependencia = this.dependenciaVisualizar.getText(); //obtenemos dependencia escrita en label
        String IDexpediente = this.IDExp.getText(); //obtenemos ID del expediente a finalizar
        
        Dependencia actual = this.adm.obtenerDependencia(nomDependencia); //obtenemos dependencia puesta por adm -> de la listaDependencias de la clase AdmDelSistema
        
        if (actual != null) {
            if (actual.existeTramite(IDexpediente)) {
                actual.finalizarTramiteConID(IDexpediente);
            }
            else{
                JOptionPane.showMessageDialog(this, "No existe expediente", "Error", JOptionPane.ERROR_MESSAGE);
                //ERROR NO EXISTE ESE EXPEDIENTE
            }
        }
        else{
            //ERROR NO EXISTE DEPENDENCIA
            JOptionPane.showMessageDialog(this, "No existe dependencia", "Error", JOptionPane.ERROR_MESSAGE);
        }
        //MENSAJE: SE HA ELIMINADO...
        JOptionPane.showMessageDialog(this, "Se ha eliminado expediente", "Éxito", JOptionPane.INFORMATION_MESSAGE);
        
        VisualizarDependencia frame = new VisualizarDependencia(this.adm);
        frame.setVisible(true);
        this.dispose();
        
    }//GEN-LAST:event_finalizarIDActionPerformed

