    private void botonBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonBuscarActionPerformed
        this.model.setRowCount(0);
        String IDdependenciaABuscar = this.dependenciaVisualizar.getText();
        Dependencia actual = this.adm.obtenerDependencia(IDdependenciaABuscar);
        if (actual != null) {
            actual.verTramitesDepedencia();
        
            String[][] datos = actual.obtenerArrayInfoTramite();
        
            for (String[] dato : datos) {
                model.addRow(dato);
            }
        }
        else{
            JOptionPane.showMessageDialog(this, "La dependencia ingresada no existe", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_botonBuscarActionPerformed
    
